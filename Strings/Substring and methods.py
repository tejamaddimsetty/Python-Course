""" 
Concept: Substring and Methods
len(String) - returns the number of characters in string
str(Object) - returns a String representation of the Object
"""

s = 'VIJAYAWADA'
# Print the letters based on Index
print('The 3rd Alphabet in VIJAYAWADA:',s[2])
# Length of the Variable value
print('The Length of the given string:',len(s))
# Print the last value in the String
print('The Last alphabet in VIJAYAWADA:',s[-1])
# Print specific part of the string
print('Specific part of the string VIJAYAWADA:', s[2:8])
# Print form specific index to end
print('Specific starting to end of the string:', s[2:])
# Print first 4 Letters of the String
print('The First 4 letters of given string:', s[:4])
